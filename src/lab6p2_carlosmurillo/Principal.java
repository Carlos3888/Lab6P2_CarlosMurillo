    private void crear_pMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_crear_pMouseClicked
        int num = combo_tipo.getSelectedIndex();

        String tipo = combo_tipo.getItemAt(num);
        String id = t_id.getText();
        String nombre = t_nombre.getText();
        int edad = Integer.parseInt(t_edad.getText());
        String sexo = "";
        if(b_masculino.isSelected()){
            sexo = "Masculino";
        }else{
            sexo = "Femenino";
        }
        String estado = "";
        if(b_soltero.isSelected()){
            estado = "Soltero";
        }else{
            estado = "Casado";
        }
        double altura = Integer.parseInt(t_altura.getText());
        double peso = Integer.parseInt(t_peso.getText());
        if(tipo.equals("Gerente")){
            String usuario = t_usuario.getText();
            String contra = t_contra.getText();
            int numc = combo_cargo.getSelectedIndex();
            String cargo = combo_cargo.getItemAt(numc);
            lista_perosnas.add(nombre);
            modelopersonas.addElement(new Personas(tipo, id, nombre, edad, sexo, estado, altura, peso, usuario, contra, cargo));
        }else{
            String ocupacion = t_ocupacion.getText();
            String horario = t_horario.getText();
            int tiempo = Integer.parseInt(t_tiempo.getText());
            double sueldo = Integer.parseInt(t_sueldo.getText());
            lista_perosnas.add(nombre);
            modelopersonas.addElement(new Personas(tipo, id, nombre, edad, sexo, estado, altura, peso, ocupacion, horario, tiempo, sueldo));
        }
        modelo_c_personas.addElement(nombre);
        lista_p.setModel(modelopersonas);
        combo_personas.setModel(modelo_c_personas);
        combo_personas_m.setModel(modelo_c_personas);

        t_id.setText("");
        t_nombre.setText("");
        t_edad.setText("");
        t_id.setText("");
        t_altura.setText("");
        t_peso.setText("");
        t_ocupacion.setText("");
        t_horario.setText("");
        t_tiempo.setText("");
        t_sueldo.setText("");
        t_usuario.setText("");
        t_contra.setText("");
    }//GEN-LAST:event_crear_pMouseClicked

